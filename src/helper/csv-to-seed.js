function parseCSV(csvText) {
  const lines = csvText.trim().split('\n');
  const headers = lines[0].split('\t');
  
  return lines.slice(1).map(line => {
    const values = line.split('\t');
    const obj = {};
    
    headers.forEach((header, index) => {
      let value = values[index]?.trim() || '';
      
      // Convertir inventario a n√∫mero
      if (header === 'INVENTARIO') {
        // Manejar casos como "2 1/4" o valores vac√≠os
        if (!value || value === '') {
          value = 0;
        } else if (value.includes(' ')) {
          // Convierte "2 1/4" a 2.25
          const parts = value.split(' ');
          const whole = parseInt(parts[0]) || 0;
          const fraction = parts[1] ? eval(parts[1]) : 0;
          value = whole + fraction;
        } else {
          value = parseInt(value) || 0;
        }
      }
      
      obj[header.toLowerCase()] = value;
    });
    
    return obj;
  });
}

function generateSeedFile(items) {
  const itemsCode = items.map(item => {
    const inventario = typeof item.inventario === 'number' ? item.inventario : 0;
    return `    { descripcion: '${item.descripci√≥n.replace(/'/g, "\\'")}', serie: '${item.serie}', categoria: '${item.categoria}', proceso: '${item.proceso}', status: '${item.status}', inventario: ${inventario} }`;
  }).join(',\n');

  return `// src/database/seeds/items.seed.ts
import { DataSource } from 'typeorm';
import { Item } from '../../modules/catalog/items/entities/item.entity';

export async function seedItems(dataSource: DataSource) {
  const itemRepo = dataSource.getRepository(Item);

  const items = [
${itemsCode}
  ];

  // Verificar si ya existen items
  const count = await itemRepo.count();
  if (count > 0) {
    console.log('‚ö†Ô∏è  Items ya existen en la base de datos. Skipping seed...');
    return;
  }

  // Insertar items en lotes de 100
  const batchSize = 100;
  for (let i = 0; i < items.length; i += batchSize) {
    const batch = items.slice(i, i + batchSize);
    await itemRepo.save(batch);
    console.log(\`‚úÖ Insertados \${i + batch.length} de \${items.length} items\`);
  }

  console.log(\`üéâ \${items.length} items insertados correctamente\`);
}
`;
}

